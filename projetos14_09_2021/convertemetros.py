#Algoritmo que ler um número em metros e convete para centimetros e milimetros.

valormetros = float(input('Digite o valor em metros: '))
centimetros = valormetros *100
milimetros = valormetros *1000

print(f'{valormetros} metros(s) corresponde(m) á {centimetros} centimetros.')
print(f'{valormetros} metros(s) corresponde(m) á {milimetros} milimetros.')

